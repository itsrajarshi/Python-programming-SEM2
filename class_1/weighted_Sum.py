# Define weights
a = 5
b = 2
c = 1

# Input quantities
x1 = int(input("Enter quantity for weight 5: "))
x2 = int(input("Enter quantity for weight 2: "))
x3 = int(input("Enter quantity for weight 1: "))

# Calculate weighted total
grand_total = (a * x1) + (b * x2) + (c * x3)

# Display result
print(f"Grand total (weighted sum): {grand_total}")